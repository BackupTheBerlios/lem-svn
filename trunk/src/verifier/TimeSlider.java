	public double getTimeFactor() {
		double value = (double) timeSlider.getValue() ;
		double timeFactor = 0.0 ;
		if (value == 100)
			timeFactor = 1.0 ; 
		else if ( value < 100)
			timeFactor = 1 -Math.abs(  (value-100) / 100 );  
		else 
			timeFactor = (double)(value - 100) ;
		
		return Math.round( (100*timeFactor) ) / 100.0   ; 
	}
	
